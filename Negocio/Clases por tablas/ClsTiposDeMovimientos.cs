            {
                try
                {
                    TipoDeMovimiento ObjetoActualizado = BBDD.TipoDeMovimiento.SingleOrDefault(Identificador => Identificador.ID_TipoDeMovimiento == _TipoDeMovimiento.ID_TipoDeMovimiento);

                    if (ObjetoActualizado != null)
